package com.hp.it.perf.ac.load.content;

public class AcLoadException extends Exception {

	private static final long serialVersionUID = 1726547382469733139L;

	public AcLoadException() {
	}

	public AcLoadException(String message, Throwable cause) {
		super(message, cause);
	}

	public AcLoadException(String message) {
		super(message);
	}

	public AcLoadException(Throwable cause) {
		super(cause);
	}

}
